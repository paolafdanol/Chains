# capitalize(), retorna una copia de la cadena con la primera letra en mayuscula
cadena = "bienvenido a mi tarea"
print(cadena.capitalize())

# center, centra un string() en una cantidad especifica de
# caracteres
print("\n")
cadena3 = "texto"
print(cadena3)
center_str = cadena3.center(20)
print(center_str)

# strip, devuelve una copia de la cadena con los espacios en blanco iniciales y finales eliminados.
print("\n")
cadena4 ="   Olitas de mar   "
print(cadena4)
eliminar_espacios = cadena4.strip()
print(eliminar_espacios)

# replace, reemplazar caracteres en una cadena
print("\n")
cadena5 = "Me gusta el color rojo"
print(cadena5)
print(cadena5.replace("rojo","morado"))

